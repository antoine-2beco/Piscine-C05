int	ft_sqrt(int nb)
{
	int	i;
	int	n;
	int	k;

	n = 1;
	i = 1;
	k = 0;
	while (i < nb)
	{
		n += 2;
		i = i + n;
		k++;
	}
	if (i != nb)
		return (0);
	else
		return (k + 1);
}

